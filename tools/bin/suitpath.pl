$DEVDIR = "$ENV{WORK}/ddpoker";
$IMGDIR = "$DEVDIR/image-src/poker/cards";
$SRCDIR = "$DEVDIR/arch/java/com/donohoedigital/games/poker";
$IMGFILE = "$IMGDIR/suit-paths.svg";
$SRCFILE = "$SRCDIR/CardSuitPaths.java";

open (SRC, ">$SRCFILE") || die "Couldn't open $SRCFILE";
print SRC "package com.donohoedigital.games.poker;\n";
print SRC "\n";
print SRC "/** This was generated by $0 - do not modify **/\n";
print SRC "\n";
print SRC "public class CardSuitPaths\n";
print SRC "{\n";

$path = "";
open (PATHS, "$IMGFILE") || die "Couldn't open $IMGFILE";
while ($line = <PATHS> )
{
	chop $line;
	$line =~ s/\s//g;
	if ($line =~ "\<path")
	{
		# get name	
		$line =~ /id=\"([a-zA-Z0-9]+)\"/;
		$name = $1;

		# remove extra data
		$line =~ s/.*d=//;
		$path = $line;
	}
	elsif ($path ne "")
	{
		$path .= $line;
		if ($line =~ "\"\/\>")
		{
			$path =~ s/\/\>//;
			print "$name: $path\n";
			print SRC "    public static final String $name = $path;\n\n";
		}
	}
	else
	{
		$path = "";
	}
}

print SRC "}\n";
close(SRC);
